package com.dabashou.point.vo;

public class PointStatsVo {
    private int totalIncome;
    private int totalExpense;
    private int monthIncome;
    private int monthExpense;

    public int getTotalIncome() { return totalIncome; }
    public void setTotalIncome(int totalIncome) { this.totalIncome = totalIncome; }
    public int getTotalExpense() { return totalExpense; }
    public void setTotalExpense(int totalExpense) { this.totalExpense = totalExpense; }
    public int getMonthIncome() { return monthIncome; }
    public void setMonthIncome(int monthIncome) { this.monthIncome = monthIncome; }
    public int getMonthExpense() { return monthExpense; }
    public void setMonthExpense(int monthExpense) { this.monthExpense = monthExpense; }
}
